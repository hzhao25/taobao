package javaBean;

public class MyAddress {
    private String name;
    private String number;
    private String address;

    public MyAddress() {
    }

    public MyAddress(String name, String number, String address) {
        this.name = name;
        this.number = number;
        this.address = address;
    }

    /**
     * 获取
     * @return name
     */
    public String getName() {
        return name;
    }

    /**
     * 设置
     * @param name
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * 获取
     * @return number
     */
    public String getNumber() {
        return number;
    }

    /**
     * 设置
     * @param number
     */
    public void setNumber(String number) {
        this.number = number;
    }

    /**
     * 获取
     * @return address
     */
    public String getAddress() {
        return address;
    }

    /**
     * 设置
     * @param address
     */
    public void setAddress(String address) {
        this.address = address;
    }

    public String toString() {
        return "MyAddress{name = " + name + ", number = " + number + ", address = " + address + "}";
    }
}
